package no.nav.foreldrepenger.tilbakekreving.dokumentbestilling.domene;

public class YtelseNavn {
    private String navnPåBrukersSpråk;
    private String navnPåBokmål;

    public String getNavnPåBrukersSpråk() {
        return navnPåBrukersSpråk;
    }

    public void setNavnPåBrukersSpråk(String navnPåBrukersSpråk) {
        this.navnPåBrukersSpråk = navnPåBrukersSpråk;
    }

    public String getNavnPåBokmål() {
        return navnPåBokmål;
    }

    public void setNavnPåBokmål(String navnPåBokmål) {
        this.navnPåBokmål = navnPåBokmål;
    }
}
